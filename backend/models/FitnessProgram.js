import mongoose from "mongoose";

const fitnessProgramSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    trim: true,
  },
  description: {
    type: String,
    required: true,
    trim: true,
  },
  exercises: [
    {
      name: {
        type: String,
        required: true,
        trim: true,
      },
      description: {
        type: String,
        required: true,
        trim: true,
      },
      duration: {
        type: Number,
        required: true,
      },
      intensity: {
        type: String,
        required: true,
        enum: ["low", "medium", "high"],
      },
    },
  ],
  createdBy: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "User",
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

export default mongoose.model("FitnessProgram", fitnessProgramSchema);
